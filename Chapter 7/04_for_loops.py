for i in range(4):  # range function generates a sequence of numbers from 0 to 3
    print(i)

for i in range(1, 5):  # range function generates a sequence of numbers from 1 to 4
    print(i)

for i in range(1, 10, 2):  # range function generates a sequence of numbers from 1 to 9 with a step of 2
    print(i)