#For loops with lists
l = [1,4,6,234,6,764]

for i in l: # for loop iterates over each element in the list l
    print(i)

#For loops with tuples
t = (6,231,75,122)
for i in t: # for loop iterates over each element in the tuple t
    print(i)

#For loops with strings
s = "Harry"
for i in s: # for loop iterates over each character in the string s
    print(i)