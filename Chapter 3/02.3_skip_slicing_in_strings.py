
word = "amazing"

wordshort = word[1: 6: 2] # Start index is inclusive and end index is exclusive. So it will include characters from index 1 to index 5 (m, a, z) and exclude the character at index 6 (g). Therefore, wordshort will be "amaz".
print(wordshort) # This line prints the value of 'wordshort', which is "amaz".